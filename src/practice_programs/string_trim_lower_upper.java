package practice_programs;

public class string_trim_lower_upper {
	public static void main(String[] args) {
		String a = "  Hello  ";

		System.out.println(a.trim() + "how are you"); // trim

		System.out.println(a.toLowerCase()); // to lower case

		System.out.println(a.toUpperCase()); // to upper case

	}
}
